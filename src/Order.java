import java.util.Vector;

public class Order {
    int ID;
    String sate;
    String state;
    String payment;
    String delivery;
    String items;
    float value;

    public Order(int ID, String sate, String state, String payment, String delivery, String items, float value) {
        this.ID = ID;
        this.sate = sate;
        this.state = state;
        this.payment = payment;
        this.delivery = delivery;
        this.items = items;
        this.value = value;
    }


}

